function ShopStock(val,id)
	if val==0 then
		local CurRound=WD.Circle()
		PosItems={}
		for i=1,table.maxn(itemlist) do
			if (itemlist[i][3]) and (itemlist[i][4]) and itemlist[i][3]<=(CurRound) then
				PosItems[#PosItems+1]=i
			end
		end
		return PosItems
	end
	if val==1 then
		for i=1,table.maxn(itemlist) do
			if (itemlist[i]) and i==id then
				local size=b.GetData(0)
				local prc
				if itemlist[i][4]==nil then
					prc=nil
				else
					prc=math.ceil(itemlist[i][4]*((math.sqrt(size))/20))
				end
				return itemlist[i][1],prc
			end
		end
	
	end
end

function ReturnInfo(id,wachuwah)
	if wachuwah==0 then
		--Name
		wahewah=(itemlist[id][1])
		return wahewah
	elseif wachuwah==1 then
		--Spell Name
		local diff=table.maxn(items)
		wahewah=(scrolls[id-diff][2])
		return wahewah
	elseif wachuwah==2 then
		-- Stat Number
		local diff=table.maxn(items)+table.maxn(scrolls)
		wahewah=(gems[id-diff][2])
		return wahewah
	elseif wachuwah==3 then
		-- Can Stack
		wahewah=(itemlist[id][2])
		return wahewah
	elseif wachuwah==4 then
		if id<=table.maxn(items) then
			-- All item info
			wahewah=0
			name=(items[id][1])
			itemuse=(items[id][2])
			stat1=(items[id][3])
			stat2=(items[id][4])
			return wahewah,name,itemuse,stat1,stat2
		elseif id<=table.maxn(items)+table.maxn(scrolls) then
			-- All scroll info
			local diff=table.maxn(items)
			wahewah=3
			name=(scrolls[id-diff][1])
			itemuse=(scrolls[id-diff][2])
			stat1=(scrolls[id-diff][3])
			return wahewah,name,itemuse,stat1
		elseif id<=table.maxn(items)+table.maxn(scrolls)+table.maxn(gems) then
			-- All gem info
			local diff=table.maxn(items)+table.maxn(scrolls)
			wahewah=4
			name=(gems[id-diff][1])
			stat1=(gems[id-diff][2])
			stat2=(gems[id-diff][3])
			return wahewah,name,stat1,stat2
		elseif id<=table.maxn(items)+table.maxn(scrolls)+table.maxn(gems)+table.maxn(special) then
			-- All special info
			local diff=table.maxn(items)+table.maxn(scrolls)+table.maxn(gems)
			wahewah=2
			name=(special[id-diff][1])
			stat1=(special[id-diff][2])
			stat2=(special[id-diff][3])
			return wahewah,name,stat1,stat2
		else
			-- All equips info
			local diff=table.maxn(items)+table.maxn(scrolls)+table.maxn(gems)+table.maxn(special)
			wahewah=1
			name=(equips[id-diff][1])
			itemuse=(equips[id-diff][2])
			stat1=(equips[id-diff][3])
			stat2=(equips[id-diff][4])
			stat3=(equips[id-diff][5])
			stat4=(equips[id-diff][6])
			stat5=(equips[id-diff][7])
			stat6=(equips[id-diff][8])
			return wahewah,name,itemuse,stat1,stat2,stat3,stat4,stat5,stat6
		end
	end
end

